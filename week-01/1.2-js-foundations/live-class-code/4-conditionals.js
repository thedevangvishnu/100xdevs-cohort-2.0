// Practice using conditionals: if/else

let firstName = "Devang";
let lastName = "Vishnu";
const isMarried = false;
const isStudent = true;

if (isMarried) {
  console.log(firstName + " " + lastName + " is not a married person!");
} else {
  console.log("He is a student.");
} // Output: He is a student

if (isStudent) {
  console.log(firstName + " " + lastName + " is not a married person!");
} else {
  console.log("He is a student.");
} // Output: Devang Vishnu is not a married person!
